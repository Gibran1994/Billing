package restaurant;

public class Item
{
	private String item;
	private int rate;
	
	//Getter Item
	public String getItem() 
	{
		return item;
	}
	
	//Setter Item
	public void setItem(String item) 
	{
		this.item = item;
	}
	
	//Getter Rate
	public int getRate() 
	{
		return rate;
	}
	
	//Setter Rate
	public void setRate(int rate) 
	{
		this.rate = rate;
	}
	
}
